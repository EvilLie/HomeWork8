namespace HomeWork8XElem
{
    public struct Person
    {
        /// <summary>
        /// Full name of person
        /// </summary>
        public string FullName { get; set; }
        /// <summary>
        /// Street where person lives
        /// </summary>
        public string Adress { get; set; }
        /// <summary>
        /// Number of person house
        /// </summary>
        public int HouseNumber { get; set; }
        /// <summary>
        /// Number of person flat
        /// </summary>
        public int FlatNumber { get; set; }
        /// <summary>
        /// Mobile phone number of person
        /// </summary>
        public string MobileNumber { get; set; }
        /// <summary>
        /// Home phone number
        /// </summary>
        public string HomePhoneNum { get; set; }
    }
}
